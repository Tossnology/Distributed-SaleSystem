package com.software.domain;

public class StoreManager {
    
    private String id;
    private String password;
    private String name;
    private String gender;
    private String phone;
    private String email;
    private String label;
    private String hourseid;
    private String hoursename;
    private String time;
    
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getHourseid() {
		return hourseid;
	}
	public void setHourseid(String hourseid) {
		this.hourseid = hourseid;
	}
	public String getHoursename() {
		return hoursename;
	}
	public void setHoursename(String hoursename) {
		this.hoursename = hoursename;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}